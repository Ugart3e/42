#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	contador;
	int	i;
	int	temp;

	contador = 0;
	while (contador < (size - 1))
	{
		i = 0;
		while (i < (size -1))
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
			}
			i++;
		}
		contador++;
	}
}
